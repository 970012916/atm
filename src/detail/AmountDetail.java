package detail;

import java.util.Date;

public class AmountDetail {
	
	private int carNun ;
	private int amount ;
	private int opType ;
	private Date opTime ;
	private String tip ;
	public int getCarNun() {
		return carNun;
	}
	public void setCarNun(int carNun) {
		this.carNun = carNun;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	public int getOpType() {
		return opType;
	}
	public void setOpType(int opType) {
		this.opType = opType;
	}
	public Date getOpTime() {
		return opTime;
	}
	public void setOpTime(Date opTime) {
		this.opTime = opTime;
	}
	public String getTip() {
		return tip;
	}
	public void setTip(String tip) {
		this.tip = tip;
	}
	
	
	
}
